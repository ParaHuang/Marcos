public class Test12_Operator {
    public static void main(String[] args) {
        //rational operator
        // >    >=    <     <=        ==        !=
        System.out.println(5>3);    //true
        System.out.println(5>=3);   //true,greater or equals
        System.out.println(5>=5);   //true,greater or equals
        System.out.println();
        System.out.println(5<8);    //true
        System.out.println(5<=8);   //true,less or equals
        System.out.println(8<=8);   //true,less or equals
        System.out.println();

        System.out.println(5==5);   //true,compare two numbers, see if they are equals as each other
        System.out.println(5!=5);   //false,compare two numbers, see if they are not equals as each other

    }
}
