public class Test6_Operator {
    public static void main(String[] args) {
        //operator:   +
        System.out.println(12+3);   //15
        System.out.println(12-3);   //9
        System.out.println(12*3);   //36        times:*   when you write on paper,it's x     on paper, 12 x 3 = 36
        System.out.println(12/3);   //4         divide:/    when you write on paper,it's ➗  on paper, 12 ➗3 = 4
        System.out.println(12%3);   //0
        //% -> module
        //resto da divisão (remainder)
        //to get remainder after divide operation

        System.out.println(32%7);   //4
        System.out.println(231%8);  //7
        System.out.println(193%6);  //1

        System.out.println(10/3);   //3        ， the integer does operation with integer, the result is still integer
    }
}
