public class Test13_Operator {
    public static void main(String[] args) {
        int a = 5;
        int b = 7;

        System.out.println(a>b);    //false
        System.out.println(a>=b);   //false
        System.out.println(a<b);    //true
        System.out.println(a<=b);   //true
        System.out.println(a==b);   //false
        System.out.println(a!=b);   //true

        //  a = 5      means save 5 in variable a
        //  a == 5     means compare a with 5, see if a equals as 5



    }
}
